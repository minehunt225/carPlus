    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = jTextField1.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        try (Connection conn = connection.MySql.getConnection()) {
            String sql = "SELECT * FROM users WHERE Username = ? AND password = ?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, user);
            statement.setString(2, password);
            ResultSet rs = statement.executeQuery();

            if (rs.next()) {
                this.dispose();
                new Home(user).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid login. Try again.");
            }
        } catch (Exception e) {
            logger.log(Level.SEVERE, "An unexpected error occurred during login", e);
            JOptionPane.showMessageDialog(this, " An unexpected error occurred during login.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

