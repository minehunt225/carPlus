    public void loadSuppliers() {
        supplierMap = new LinkedHashMap<>();
        Connection conn = null;
        PreparedStatement pStatement = null;
        ResultSet rs = null;

        try {
            conn = MySql.getConnection();
            String query = "SELECT SupplierID, CompanyName FROM suppliers ORDER BY CompanyName";
            pStatement = conn.prepareStatement(query);
            rs = pStatement.executeQuery();

            supplier.removeAllItems(); 
            while (rs.next()) {
                int supplierId = rs.getInt("SupplierID"); 
                String supplierName = rs.getString("CompanyName"); 
                supplierMap.put(supplierName, supplierId); 
                supplier.addItem(supplierName); 
            }
            if (supplier.getItemCount() == 0) {
                supplier.addItem("No Suppliers Found"); 
            }

        } catch (SQLException e) {
            logger.log(Level.SEVERE, "Database error loading suppliers", e);
        } catch (Exception e) {
            logger.log(Level.SEVERE, "Unexpected error loading suppliers", e);
        }
    }

