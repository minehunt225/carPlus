    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String carBrand = brand.getText().trim();
        String carModel = modal.getText().trim();
        int carYear = (Integer) year.getValue();
        String carColor = color.getText().trim();
        String priceText = price.getText().trim();
        String selectedSupplierName = (String) supplier.getSelectedItem();

        if (carBrand.isEmpty() || carModel.isEmpty() || carColor.isEmpty() || priceText.isEmpty() || selectedSupplierName == null || selectedSupplierName.equals("No Suppliers Found")) {
            JOptionPane.showMessageDialog(this, "Please fill in all car details and select a valid supplier.", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double carPrice;
        try {
            carPrice = Double.parseDouble(priceText);
            if (carPrice <= 0) {
                JOptionPane.showMessageDialog(this, "Price must be a positive number.", "Input Error", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid price format. Please enter a valid number.", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Integer supplierId = supplierMap.get(selectedSupplierName);
        if (supplierId == null) {
            JOptionPane.showMessageDialog(this, "Selected supplier is invalid. Please select from the list.", "Supplier Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Connection conn = null;
        PreparedStatement pStatement = null;
        try {
            conn = MySql.getConnection();
            String query = "INSERT INTO vehicles (Make, Model, Year, Color, Price, SupplierID, quantity) VALUES (?, ?, ?, ?, ?, ?, ?)";
            pStatement = conn.prepareStatement(query);
            pStatement.setString(1, carBrand); 
            pStatement.setString(2, carModel);
            pStatement.setInt(3, carYear);
            pStatement.setString(4, carColor);
            pStatement.setDouble(5, carPrice);
            pStatement.setInt(6, supplierId); 
            pStatement.setInt(7, 1); 

            int rowsAffected = pStatement.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Car added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                brand.setText("");
                modal.setText("");
                year.setValue(2000); 
                color.setText("");
                price.setText("");
                supplier.setSelectedIndex(0); 
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add car. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            logger.log(Level.SEVERE, "Database error adding car", e);
        } catch (Exception e) {
            logger.log(Level.SEVERE, "Unexpected error adding car", e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

