    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String supplierName = name.getText().trim();
        String supplierCompany = company.getText().trim();
        String supplierMobile = mobile.getText().trim();
        String supplierEmail = email.getText().trim();

        if (supplierName.isEmpty() || supplierCompany.isEmpty() || supplierMobile.isEmpty() || supplierEmail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all supplier details.", "Missing Information", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!supplierEmail.matches("^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address.", "Invalid Email", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!supplierMobile.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid 10-digit mobile number (digits only).", "Invalid Mobile Number", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = connection.MySql.getConnection(); PreparedStatement pStatement = conn.prepareStatement("INSERT INTO suppliers (CompanyName, ContactName, Phone, Email) VALUES (?, ?, ?, ?)")) {

            pStatement.setString(1, supplierCompany);
            pStatement.setString(2, supplierName);
            pStatement.setString(3, supplierMobile);
            pStatement.setString(4, supplierEmail);

            int rowsAffected = pStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Supplier added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                name.setText("");
                company.setText("");
                mobile.setText("");
                email.setText("");
                loadSupplierDate();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add supplier.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            logger.log(Level.SEVERE, "Error adding supplier data: " + ex.getMessage(), ex);
            JOptionPane.showMessageDialog(this, "Error adding supplier: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a supplier to update.", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if ("No Suppliers Available".equals(model.getValueAt(selectedRow, 2))) {
            JOptionPane.showMessageDialog(this, "Cannot update 'No Suppliers Available' entry.", "Invalid Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int supplierId = (int) model.getValueAt(selectedRow, 0);

        String supplierName = name.getText().trim();
        String supplierCompany = company.getText().trim();
        String supplierMobile = mobile.getText().trim();
        String supplierEmail = email.getText().trim();

        if (supplierName.isEmpty() || supplierCompany.isEmpty() || supplierMobile.isEmpty() || supplierEmail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all supplier details.", "Missing Information", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!supplierEmail.matches("^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address.", "Invalid Email", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!supplierMobile.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid 10-digit mobile number (digits only).", "Invalid Mobile Number", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = connection.MySql.getConnection(); PreparedStatement pStatement = conn.prepareStatement("UPDATE suppliers SET ContactName = ?, CompanyName = ?, Phone = ?, Email = ? WHERE SupplierID = ?")) {

            pStatement.setString(1, supplierName);
            pStatement.setString(2, supplierCompany);
            pStatement.setString(3, supplierMobile);
            pStatement.setString(4, supplierEmail);
            pStatement.setInt(5, supplierId);

            int rowsAffected = pStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Supplier updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                name.setText("");
                company.setText("");
                mobile.setText("");
                email.setText("");
                loadSupplierDate();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update supplier. Supplier ID might not exist.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            logger.log(Level.SEVERE, "Error updating supplier data: " + ex.getMessage(), ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


