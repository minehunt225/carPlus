    private void loadSupplierDate() {
        DefaultTableModel tModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tModel.addColumn("Supplier ID");
        tModel.addColumn("Name");
        tModel.addColumn("Company");
        tModel.addColumn("Mobile Number");
        tModel.addColumn("Email");

        try (Connection conn = connection.MySql.getConnection(); PreparedStatement pStatement = conn.prepareStatement("SELECT SupplierID, ContactName, CompanyName, Phone, Email FROM suppliers"); ResultSet rs = pStatement.executeQuery()) {

            while (rs.next()) {
                int supplierId = rs.getInt("SupplierID");
                String name = rs.getString("ContactName");
                String company = rs.getString("CompanyName");
                String mobile = rs.getString("Phone");
                String email = rs.getString("Email");

                tModel.addRow(new Object[]{
                    supplierId,
                    name,
                    company,
                    mobile,
                    email
                });
            }

            if (tModel.getRowCount() == 0) {
                tModel.addRow(new Object[]{"", "", "No Suppliers Available", "", ""});
            }

            jTable1.setModel(tModel);

            jTable1.setShowGrid(true);
            jTable1.setGridColor(new Color(200, 200, 200));
            jTable1.setRowHeight(30);

            JTableHeader header = jTable1.getTableHeader();
            header.setBackground(new Color(65, 90, 119));
            header.setForeground(Color.WHITE);
            header.setFont(new Font("Segoe UI", Font.BOLD, 15));
            header.setPreferredSize(new Dimension(header.getWidth(), 35));

            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

            for (int i = 0; i < jTable1.getColumnCount(); i++) {
                jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
            }

            jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (!e.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
                        selectedRow();
                    }
                }
            });

        } catch (SQLException e) {
            logger.log(Level.SEVERE, "SQL error loading supplier data", e);
            JOptionPane.showMessageDialog(this, "Error loading supplier data: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            logger.log(Level.SEVERE, "Unexpected error loading supplier table data", e);
            JOptionPane.showMessageDialog(this, "An unexpected error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void selectedRow() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            String Name = (String) model.getValueAt(selectedRow, 1);
            String Company = (String) model.getValueAt(selectedRow, 2);
            String Mobile = (String) model.getValueAt(selectedRow, 3);
            String Email = (String) model.getValueAt(selectedRow, 4);

            name.setText(Name);
            company.setText(Company);
            mobile.setText(Mobile);
            email.setText(Email);
        }

    }

